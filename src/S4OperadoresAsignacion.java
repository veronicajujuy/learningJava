public class S4OperadoresAsignacion {
    public static void main(String[] args) {
        // asignacion
        int j=1, i=4;

        i+=5; // es i = i + 5
        System.out.println("i = " + i);
        i-=1; // es i = i -1
        System.out.println("i = " + i);
        i/=j+1; // es i = i/(j+1)
        System.out.println("i = " + i);
        
        String unString = "Hola ";
        unString += "soy un hermoso ";
        unString += "String xD";
        System.out.println("unString = " + unString);


    }
}
